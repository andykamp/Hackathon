def Uka_rate(yolo,gammel,prippen):
    distances = [-1]*5
    for i,rating in enumerate(arr):
        distances[i] = (rating[0]-yolo)**2 + (rating[1]-gammel)**2 + (rating[2]-prippen)**2

    return distances

def sort_by_distance(distances):
    names = ['Martin Garrix', 'Chillspising', 'Kulinarisk Mordhistorie', 'Jubileums kavalkade', 'Pirum OldBoys']
    tuple_list = []
    sort_i = sorted(range(len(distances)), key=lambda k: distances[k])
    for i,s_i in enumerate(sort_i):
        percent= round( (300-distances[s_i])*100/300, 2)
        tuple_list.append([names[s_i],percent])
    return tuple_list


def sort_rated(yolo,gammel,prippen):
    return sort_by_distance(Uka_rate(yolo,gammel,prippen))

